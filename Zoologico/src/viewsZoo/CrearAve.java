    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        String nombre = jTextField1.getText();
        int cantAnimal = jSpinner1.getComponentCount();
        int cantHembras = jSpinner2.getComponentCount();
        double peso = Integer.parseInt(jTextField2.getText());
        String mesApareamiento = jTextField3.getText();
        int edad = Integer.parseInt(jTextField4.getText());
        double estatura = Integer.parseInt(jTextField5.getText());
        String colorPluma = jTextField6.getText();
        int tiempoNacer = Integer.parseInt(jTextField7.getText());
        String colorHuevo = jTextField8.getText();
        double diametroHuevo = Integer.parseInt(jTextField9.getText());
        String colorCaracteristico = jTextField10.getText();
        boolean puedeVolar = true;
        if  (jRadioButton1.isSelected())
            puedeVolar = true;
        if(jRadioButton2.isSelected())
            puedeVolar = false;
       
        
        Ave ave = new Ave(estatura, colorPluma, puedeVolar, colorHuevo, diametroHuevo, tiempoNacer, nombre, cantAnimal, cantHembras, peso, tiempoNacer, edad);
        
        viewZoo.getZooService().agregarNuevaAve(estatura, colorPluma, puedeVolar, colorHuevo, diametroHuevo, tiempoNacer, nombre, cantAnimal, cantHembras, peso, tiempoNacer, edad);
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed

